async def send_chunked(event, text, chunk_size=3500):
    chunks = []
    while text:
        chunks.append(text[:chunk_size])
        text = text[chunk_size:]
    if chunks:
        await event.edit(chunks[0])
        for ch in chunks[1:]:
            await event.respond(ch)

